first_number = 3
second_number = 33
qty_numbers = 2

arithmetic_mean = (first_number + second_number) / qty_numbers
geometric_mean = pow(first_number * second_number, 1/qty_numbers)

print(f"Cреднее арифметическое заданных чисел: {arithmetic_mean}")
print(f"Cреднее геометрическое заданных чисел: {geometric_mean}")